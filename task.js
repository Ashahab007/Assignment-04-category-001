function validEmail(email) {
  if (typeof email === "string") {
    if (
      !email.startsWith(".") &&
      !email.startsWith("-") &&
      !email.startsWith("+") &&
      !email.startsWith("_") &&
      !email.startsWith("@") &&
      !email.includes(" ") &&
      email.endsWith(".com") &&
      email.includes("@")
    ) {
      return true;
    } else {
      return false;
    }
  } else {
    return "Invalid";
  }
}

