function calculateWatchTime(times) {
  if (Array.isArray(times)) {
    let isNumber = times.every((val) => typeof val === "number");

    if (isNumber) {
      let total = 0;
      for (const time of times) {
        total += time;
      }
      let hours = Math.floor(total / 3600);
      let minutes = Math.floor((total % 3600) / 60);
      let seconds = Math.floor(total % 60);

      let totalTimes = {};
      totalTimes.hour = hours;
      totalTimes.minute = minutes;
      totalTimes.second = seconds;
      return totalTimes;
    } else {
      return "Invalid";
    }
  } else {
    return "Invalid";
  }
}

