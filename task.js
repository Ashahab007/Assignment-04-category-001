function electionResult(votes) {
  if (Array.isArray(votes)) {
    let countMango = 0;
    let countBanana = 0;
    for (const element of votes) {
      if (element === "mango") {
        countMango++;
      } else if (element === "banana") {
        countBanana++;
      }
    }

    if (countMango > countBanana) {
      return "Mango";
    } else if (countBanana > countMango) {
      return "Banana";
    } else if (countBanana === countMango) {
      return "Draw";
    }
  } else {
    return "Invalid";
  }
}

